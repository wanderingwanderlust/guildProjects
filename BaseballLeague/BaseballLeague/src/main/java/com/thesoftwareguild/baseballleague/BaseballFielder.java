package com.thesoftwareguild.baseballleague;

/**
 *
 * @author apprentice
 */
public class BaseballFielder {
    private String name;
    private String position;
    private String teamName;
    private String throwingArm;
    private Integer jerseyNumber;
    private Double battingAvg;
    private Integer homeRuns;
    private Integer strikeouts;
    private Integer hits;
    private Integer stolenBases;
    private Integer fieldingErrors;
    private String battingSide;
    private Integer walks;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getTeamName() {
        return teamName;
    }

    public void setTeamName(String teamName) {
        this.teamName = teamName;
    }

    public String getThrowingArm() {
        return throwingArm;
    }

    public void setThrowingArm(String throwingArm) {
        this.throwingArm = throwingArm;
    }

    public Integer getJerseyNumber() {
        return jerseyNumber;
    }

    public void setJerseyNumber(Integer jerseyNumber) {
        this.jerseyNumber = jerseyNumber;
    }

    public Double getBattingAvg() {
        return battingAvg;
    }

    public void setBattingAvg(Double battingAvg) {
        this.battingAvg = battingAvg;
    }

    public Integer getHomeRuns() {
        return homeRuns;
    }

    public void setHomeRuns(Integer homeRuns) {
        this.homeRuns = homeRuns;
    }

    public Integer getStrikeouts() {
        return strikeouts;
    }

    public void setStrikeouts(Integer strikeouts) {
        this.strikeouts = strikeouts;
    }

    public Integer getHits() {
        return hits;
    }

    public void setHits(Integer hits) {
        this.hits = hits;
    }

    public Integer getStolenBases() {
        return stolenBases;
    }

    public void setStolenBases(Integer stolenBases) {
        this.stolenBases = stolenBases;
    }

    public Integer getFieldingErrors() {
        return fieldingErrors;
    }

    public void setFieldingErrors(Integer fieldingErrors) {
        this.fieldingErrors = fieldingErrors;
    }

    public String getBattingSide() {
        return battingSide;
    }

    public void setBattingSide(String battingSide) {
        this.battingSide = battingSide;
    }

    public Integer getWalks() {
        return walks;
    }

    public void setWalks(Integer walks) {
        this.walks = walks;
    }
    
}
